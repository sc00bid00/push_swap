#include <push_swap.h>

void	rrr(t_stk **sta, t_stk **stb, t_var *var)
{
	rra(sta, 0, var);
	rrb(stb, 0, var);
	var->scr++;
	if (var->prt)
		ft_printf("rrr\n");
}
